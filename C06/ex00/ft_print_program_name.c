#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str)
		write(1, str++, 1);
}

int	main(int argc, char **argv)
{	
	if (argc == 2)
		ft_putstr(argv[1]);
	write(1, "\n", 1);
}
